function searchGuide() {
    const selectedGuide = guideSelect.value;
    const tbody = document.querySelector("#resultTable tbody");
    tbody.innerHTML = "";

    if (!selectedGuide) {
        alert("Please select a guide name");
        return;
    }

    const filtered = students.filter(s => s.guide === selectedGuide);

    document.getElementById("resultCount").innerText =
        `Total Students under "${selectedGuide}": ${filtered.length}`;

    filtered.forEach(s => {
        const row = `<tr>
            <td>${s.enrollment_no}</td>
            <td>${s.name}</td>
            <td>${s.email}</td>
            <td>${s.project_title}</td>
            <td>${s.guide}</td>
	    <td>${s.co_guide}</td>
        </tr>`;
        tbody.innerHTML += row;
    });

    document.getElementById("resultTable").style.display = "table";
}


